# Factorial Example
"""
The factorial of a positive integer n is the product of all the positive integers that are
less than or equal to n.

n! = n * (n - 1) * (n - 2) * .... * 1

That is:

5! = 5 * 4 * 3 * 2 * 1 = 120
"""